/**
 * Sums three numbers together
 * @param {number} a - First number
 * @param {number} b - Second number
 * @param {number} c - Third number
 * @returns {number} The sum of all three numbers
 */
function sum3Numbers(a, b, c) {
    return a + b + c;
    // return a + b + c;
}

